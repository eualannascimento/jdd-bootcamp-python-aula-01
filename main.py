# Testando os primeiros comandos do Python

# print() - imprime algo no terminal
print("Hello World!") 
print(3)
print(3 + 4)
print("Oi" + " " + "Turma")
print(True)

# input() - pede pro usuário algum dado para entrada
print("Oi" + " " +  input("Digite seu nome: "))

# len() - conta a quantidade de caracteres de uma string
print(len("Alan")) # Irá retornar 4

# type() - imprime o tipo do que você desejar verificar
print(type(3)) # Irá retornar int